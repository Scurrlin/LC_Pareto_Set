# Hash Tables

# A hash table is a data structure that stores key-value pairs. It uses a hash
# function to compute an index into an array of buckets or slots, from which the
# desired value can be found.

hash_table = {}
hash_table['key1'] = 'value1'
hash_table['key2'] = 'value2'
print(hash_table['key1'])  # Output: 'value1'
